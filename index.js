//adding different types
console.log("2" + true);
console.log("2" + 3);
console.log(3 + true);
//adding different types

//multiplication different types
console.log("2" * true);
console.log("2" * 3);
console.log(3 * true);
//multiplication different types

//division different types
console.log("2" / true);
console.log("2" / 3);
console.log(3 / true);
//division different types

//explicit conversion
console.log(Number("123"));
console.log(String(555));
console.log(Boolean("2"));
//explicit conversion